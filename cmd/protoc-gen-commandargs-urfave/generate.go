func generateScalarAssignment(g *protogen.GeneratedFile, field *protogen.Field, receiver string) {
	flagName := plugin.FlagNameForField(field)
	assign := receiver + "." + field.GoName
	if field.Desc.IsList() {
		if needsSliceCast(field.Desc) {
			getter := listGetter(field.Desc, flagName)
			g.P("	{")
			g.P("		raw := ", getter)
			g.P("		conv := make([]int32, len(raw))")
			g.P("		for i, v := range raw {")
			g.P("			conv[i] = int32(v)")
			g.P("		}")
			g.P("		", assign, " = conv")
			g.P("	}")
		} else {
			g.P("	", assign, " = ", listGetter(field.Desc, flagName))
		}
	} else {
		getter := scalarGetter(field.Desc, flagName)
		if needsCast(field.Desc) {
			g.P("	", assign, " = ", castExpr(field.Desc, getter))
		} else {
			g.P("	", assign, " = ", getter)
		}
	}
	opts := plugin.GetFlagOptions(field)
	if opts != nil && len(opts.GetAllowedValues()) > 0 {
		generateAllowedValuesCheck(g, assign, flagName, opts.GetAllowedValues(), field.Desc.IsList())
	}
}

func generateAllowedValuesCheck(g *protogen.GeneratedFile, assignExpr string, flagName string, allowedValues []string, isList bool) {
	fmtSprint := protogen.GoIdent{GoName: "Sprint", GoImportPath: "fmt"}
	fmtErrorf := protogen.GoIdent{GoName: "Errorf", GoImportPath: "fmt"}
	if isList {
		g.P("	{")
		g.P("		allowed := map[string]bool{")
		for _, v := range allowedValues {
			g.P("			", quote(v), ": true,")
		}
		g.P("		}")
		g.P("		for _, v := range ", assignExpr, " {")
		g.P("			if !allowed[", fmtSprint, "(v)] {")
		g.P(`				return nil, `, fmtErrorf, `("invalid value %q for flag `, flagName, `, allowed: %v", v, allowed)`)
		g.P("			}")
		g.P("		}")
		g.P("	}")
	} else {
		g.P("	if c.IsSet(", quote(flagName), ") {")
		g.P("		allowed := []string{", joinQuoted(allowedValues), "}")
		g.P("		valid := false")
		g.P("		for _, a := range allowed {")
		g.P("			if ", fmtSprint, "(", assignExpr, ") == a {")
		g.P("				valid = true")
		g.P("				break")
		g.P("			}")
		g.P("		}")
		g.P("		if !valid {")
		g.P(`			return nil, `, fmtErrorf, `("invalid value %q for flag `, flagName, `, allowed: %v", `, fmtSprint, `(`, assignExpr, `), allowed)`)
		g.P("		}")
		g.P("	}")
	}
}

func generateNewCommand(g *protogen.GeneratedFile, ci plugin.CommandInfo, exportedName string, flagsFuncName string) {
	g.P("func New", exportedName, "Command(action cli.ActionFunc, subcommands ...*cli.Command) *cli.Command {")
	g.P("	return &cli.Command{")
	g.P("		Name: ", quote(ci.Name), ",")
	if ci.Description != "" {
		g.P("		Description: ", quote(ci.Description), ",")
	}
	if ci.Usage != "" {
		g.P("		Usage: ", quote(ci.Usage), ",")
	}
	if len(ci.Aliases) > 0 {
		g.P("		Aliases: []string{", joinQuoted(ci.Aliases), "},")
	}
	if ci.Hidden {
		g.P("		Hidden: true,")
	}
	if ci.Category != "" {
		g.P("		Category: ", quote(ci.Category), ",")
	}
	g.P("		Flags:       ", flagsFuncName, "(),")
	g.P("		Subcommands: subcommands,")
	g.P("		Action:      action,")
	g.P("	}")
	g.P("}")
}

