func generateFile(gen *protogen.Plugin, file *protogen.File) error {
	commandMsgs := plugin.CollectCommandMessages(file)
	if len(commandMsgs) == 0 {
		return nil
	}

	filename := file.GeneratedFilenamePrefix + "_commandargs.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)

	g.P("// Code generated by protoc-gen-commandargs-urfave. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()
	g.P(`import "github.com/urfave/cli/v2"`)

	for _, msg := range commandMsgs {
		if err := plugin.ValidateMessage(msg); err != nil {
			return err
		}
	}

	for _, msg := range commandMsgs {
		ci, err := plugin.ExtractCommand(msg)
		if err != nil {
			return err
		}
		exportedName := string(msg.Desc.Name())
		flagsFuncName := exportedName + "CommandFlags"

		g.P()
		generateFlagsFunc(g, ci.Flags, flagsFuncName)
		g.P()
		generateParseFunc(g, msg, exportedName)
		g.P()
		generateNewCommand(g, ci, exportedName, flagsFuncName)
		g.P()
		generateNewApp(g, ci, exportedName, flagsFuncName)
	}

	return nil
}

func generateFlagsFunc(g *protogen.GeneratedFile, flags []plugin.FlagInfo, funcName string) {
	g.P("func ", funcName, "() []cli.Flag {")
	if len(flags) == 0 {
		g.P("	return nil")
	} else {
		g.P("	return []cli.Flag{")
		for _, f := range flags {
			generateFlag(g, f, "		")
		}
		g.P("	}")
	}
	g.P("}")
}

func generateFlag(g *protogen.GeneratedFile, f plugin.FlagInfo, indent string) {
	cliType := flagTypeToCLI(f.Type)
	g.P(indent, "&cli.", cliType, "{")
	g.P(indent, "	Name: ", quote(f.Name), ",")
	if f.Usage != "" {
		g.P(indent, "	Usage: ", quote(f.Usage), ",")
	}
	if len(f.Aliases) > 0 {
		g.P(indent, "	Aliases: []string{", joinQuoted(f.Aliases), "},")
	}
	if f.Default != "" {
		generateDefaultValue(g, f, indent+"	")
	}
	if f.Required {
		g.P(indent, "	Required: true,")
	}
	if f.Hidden {
		g.P(indent, "	Hidden: true,")
	}
	if f.Category != "" {
		g.P(indent, "	Category: ", quote(f.Category), ",")
	}
	g.P(indent, "},")
}

func generateDefaultValue(g *protogen.GeneratedFile, f plugin.FlagInfo, indent string) {
	switch f.Type {
	case plugin.FlagTypeStringList:
		elems := plugin.SplitDefault(f.Default)
		g.P(indent, "Value: cli.NewStringSlice(", joinQuoted(elems), "),")
	case plugin.FlagTypeIntList:
		g.P(indent, "Value: cli.NewIntSlice(", f.Default, "),")
	case plugin.FlagTypeInt64List:
		g.P(indent, "Value: cli.NewInt64Slice(", f.Default, "),")
	case plugin.FlagTypeDuration:
		timeParseDuration := protogen.GoIdent{GoName: "ParseDuration", GoImportPath: "time"}
		timeDuration := protogen.GoIdent{GoName: "Duration", GoImportPath: "time"}
		g.P(indent, "Value: func() ", timeDuration, " { d, _ := ", timeParseDuration, "(", quote(f.Default), "); return d }(),")
	case plugin.FlagTypeBool, plugin.FlagTypeInt, plugin.FlagTypeInt64, plugin.FlagTypeFloat64:
		g.P(indent, "Value: ", f.Default, ",")
	default:
		g.P(indent, "Value: ", quote(f.Default), ",")
	}
}

func generateParseFunc(g *protogen.GeneratedFile, msg *protogen.Message, exportedName string) {
	g.P("func Parse", exportedName, "(c *cli.Context) (*", msg.GoIdent, ", error) {")
	g.P("	cfg := &", msg.GoIdent, "{}")
	generateParseAssignments(g, msg, "cfg")
	g.P("	return cfg, nil")
	g.P("}")
}

func generateParseAssignments(g *protogen.GeneratedFile, msg *protogen.Message, receiver string) {
	for _, field := range msg.Fields {
		wkt, isWKT := plugin.GetWellKnownType(field)
		if field.Message != nil && !isWKT {
			subReceiver := receiver + "." + field.GoName
			g.P("	", subReceiver, " = &", field.Message.GoIdent, "{}")
			generateParseAssignments(g, field.Message, subReceiver)
		} else if isWKT && field.Desc.IsList() {
			generateRepeatedWKTAssignment(g, field, receiver)
		} else if isWKT {
			generateWKTAssignment(g, field, wkt, receiver)
		} else {
			generateScalarAssignment(g, field, receiver)
		}
	}
}

func generateWKTAssignment(g *protogen.GeneratedFile, field *protogen.Field, wkt plugin.WellKnownTypeInfo, receiver string) {
	flagName := plugin.FlagNameForField(field)
	assign := receiver + "." + field.GoName
	fullName := string(field.Message.Desc.FullName())

	switch fullName {
	case "google.protobuf.Duration":
		wrapIdent := protogen.GoIdent{GoName: "New", GoImportPath: "google.golang.org/protobuf/types/known/durationpb"}
		g.P("	", assign, " = ", wrapIdent, "(c.Duration(", quote(flagName), "))")
	case "google.protobuf.Timestamp":
		timeParseIdent := protogen.GoIdent{GoName: "Parse", GoImportPath: "time"}
		rfc3339Ident := protogen.GoIdent{GoName: "RFC3339", GoImportPath: "time"}
		tsNewIdent := protogen.GoIdent{GoName: "New", GoImportPath: "google.golang.org/protobuf/types/known/timestamppb"}
		fmtErrorf := protogen.GoIdent{GoName: "Errorf", GoImportPath: "fmt"}
		g.P("	if v := c.String(", quote(flagName), "); v != \"\" {")
		g.P("		t, err := ", timeParseIdent, "(", rfc3339Ident, ", v)")
		g.P("		if err != nil {")
		g.P(`			return nil, `, fmtErrorf, `("invalid timestamp for flag `, flagName, `: %w", err)`)
		g.P("		}")
		g.P("		", assign, " = ", tsNewIdent, "(t)")
		g.P("	}")
	default:
		wrapFunc := wrapperFuncs[fullName]
		wrapIdent := protogen.GoIdent{GoName: wrapFunc.funcName, GoImportPath: "google.golang.org/protobuf/types/known/wrapperspb"}
		getter := wrapFunc.getter + "(" + quote(flagName) + ")"
		if wrapFunc.castFunc != "" {
			g.P("	", assign, " = ", wrapIdent, "(", wrapFunc.castFunc, "(", getter, "))")
		} else {
			g.P("	", assign, " = ", wrapIdent, "(", getter, ")")
		}
	}
}

